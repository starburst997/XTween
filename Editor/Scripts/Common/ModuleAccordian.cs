using UnityEngine;
using UnityEditor;
using System.Collections;
using System.Collections.Generic;
using Toki.Editors.EditorWindows;

public class ModuleAccordian : EditorWindowModuleBase
{
	/************************************************************************
	*	 	 	 	 	Static Variable Declaration	 	 	 	 	 	    *
	************************************************************************/
	
	/************************************************************************
	*	 	 	 	 	Static Method Declaration	 	 	 	     	 	*
	************************************************************************/
	
	/************************************************************************
	*	 	 	 	 	Private Variable Declaration	 	 	 	 	 	*
	************************************************************************/
	
	/************************************************************************
	*	 	 	 	 	Protected Variable Declaration	 	 	 	 	 	*
	************************************************************************/
	protected bool _changed = false;
    protected bool _open = false;
	
	/************************************************************************
	*	 	 	 	 	Public Variable Declaration	 	 	 	 	 		*
	************************************************************************/
	
	/************************************************************************
	*	 	 	 	 	Getter & Setter Declaration	 	 	 	 	 		*
	************************************************************************/
	public bool open
    {
        set
        {
            this._open = value;
        }
        get
        {
            return this._open;
        }
    }

	public bool changed
    {
        get
        {
            return this._changed;
        }
    }
	
	/************************************************************************
	*	 	 	 	 	Life Cycle Method Declaration	 	 	 	 	 	*
	************************************************************************/
	
	/************************************************************************
	*	 	 	 	 	Coroutine Declaration	 	  			 	 		*
	************************************************************************/
	
	/************************************************************************
	*	 	 	 	 	Private Method Declaration	 	 	 	 	 		*
	************************************************************************/
	
	/************************************************************************
	*	 	 	 	 	Protected Method Declaration	 	 	 	 	 	*
	************************************************************************/
	
	/************************************************************************
	*	 	 	 	 	Event Method Declaration	 	 	 	     	 	*
	************************************************************************/
	//============================== Ani ====================================
	//============================== Net ====================================
	//============================== UI =====================================
	
	/************************************************************************
	*	 	 	 	 	Public Method Declaration	 	 	 	 	 		*
	************************************************************************/
	public override void OnDataReady()
	{
		this._changed = false;
	}

	public override void OnGUI()
    {
        if (GUILayout.Button( this.ModuleName, "BoldLabel", GUILayout.Height(20f) ))
        {
            this._open = !this._open;
            ((EditorWindowAccordian)this._window).OpenChanged();
        }
    }

	public virtual void Save()
    {
        this._changed = false;
    }
	
}